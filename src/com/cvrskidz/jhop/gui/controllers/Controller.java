package com.cvrskidz.jhop.gui.controllers;

/**
 * The Controller interface declares the minimum behavior a JHopController needs
 * to implement to link a view to a controller.
 * 
 * @author cvrskidz 18031335
 */
public interface Controller {
    /**
     * Link all the applicable components of the view to this controller.
     */
    public void link();
}
